#OPERADORES ARITMETICOS CON PYTHON

#suma
print(5 + 5)

#resta
print(5 - 5)

#multiplicación
print(5 * 5)

#División
print(21 / 5)

#división para obtener el divisor entero
print(21 // 5)

#obtener el mod o residuo de una división
print(21 % 5)

#potencia (**)
print(2 ** 5) # dos elevado a la quinta


